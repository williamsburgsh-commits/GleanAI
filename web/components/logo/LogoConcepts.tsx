/**
 * Eight GleanAI logo concept marks — pixel SVG on a crisp grid.
 * Each is self-contained for preview / export at any size.
 */

function Grid({
  w,
  h,
  children,
  className = '',
}: {
  w: number;
  h: number;
  children: React.ReactNode;
  className?: string;
}) {
  return (
    <svg
      viewBox={`0 0 ${w} ${h}`}
      width="100%"
      height="100%"
      preserveAspectRatio="xMidYMid meet"
      shapeRendering="crispEdges"
      className={className}
      style={{ imageRendering: 'pixelated', display: 'block' }}
    >
      {children}
    </svg>
  );
}

function R(list: [number, number][], fill: string) {
  return list.map(([x, y], i) => (
    <rect key={i} x={x} y={y} width={1} height={1} fill={fill} />
  ));
}

